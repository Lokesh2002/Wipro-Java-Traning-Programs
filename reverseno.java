package wiproproject;

import java.util.*;
public class reverseno {
	static void PrintReverseOrder(int N)
	{
	 
	    for (int i = N; i >= 0; i--)
	        System.out.print( +i + " ");
	}
	public static void main(String[] args)
	{
	    int N = 100;
	 
	    PrintReverseOrder(N);
	}
	 

}
